int	ft_atoi(const char *str)
{
	char	*s;
	int		sign;
	int		res;

	sign = 1;
	res = 0;
	s = (char *) str;
	while ((*s >= 9 && *s <= 13) || *s == ' ')
		s ++;
	if (*s == '-')
		sign *= -1;
	if (*s == '-' || *s == '+')
		s ++;
	while (*s >= 48 && *s <= 57)
	{
		res = res * 10 + sign * (*s - 48);
		s++;
	}
	return (res);
}

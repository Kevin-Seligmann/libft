int	ft_atoi(const char *str)
{
	char	*s;
	int		i_sign;
	int		i_res;

	i_sign = 1;
	i_res = 0;
	s = (char *) str;
	while ((*s >= 9 && *s <= 13) || *s == ' ')
		s ++;
	if (*s == '-')
		i_sign *= -1;
	if (*s == '-' || *s == '+')
		s ++;
	while (*s >= '0' && *s <= '9')
	{
		i_res = i_res * 10 + i_sign * (*s - 48);
		s++;
	}
	return (i_res);
}
